/// An enumeration representing the types of sensors available.
///
/// The `SensorType` enum includes the following values:
///
/// - `vibration`: Represents a sensor that measures vibration.
/// - `energy`: Represents a sensor that measures energy consumption.
enum SensorType {
  /// Represents a sensor that measures vibration.
  vibration,

  /// Represents a sensor that measures energy consumption.
  energy,
}
